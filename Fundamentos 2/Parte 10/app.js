// Comparadores 

const numero1 = 20;
const numero2 = 50;
const numero3 = '20';

console.log(numero1 < numero2);
console.log(numero1 > numero2);
console.log(numero1 == numero3);    //Revisa solo los datos
console.log(numero1 === numero3);   //Revisa los datos y el 
                                    //tipo de variable 


console.log(numero1 != numero3);


console.log(null == undefined);     //Comparador normal
console.log(null === undefined);    //Comparador extrito
