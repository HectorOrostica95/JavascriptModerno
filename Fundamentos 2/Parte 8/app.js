// Números en Javascript

const numero1 = 30,
      numero2 = 20,
      numero3 = 20.20,
      numero4 = .1020,
      numero5 = -3;


let resultado;

//Suma
resultado = numero1 + numero2
console.log('Suma = ' + resultado);

//resta
resultado = numero1 - numero2
console.log('resta = ' + resultado);

//Multiplicación
resultado = numero1 * numero2
console.log('Multiplicación = ' + resultado);

//División
resultado = numero1 / numero2
console.log('División = ' + resultado);

//Modulo
resultado = numero1 % numero2
console.log('Modulo = ' + resultado);
